import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var purplyBlue: UIColor {
        return UIColor(red: 117.0 / 255.0, green: 3.0 / 255.0, blue: 253.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var marigold: UIColor {
        return UIColor(red: 253.0 / 255.0, green: 187.0 / 255.0, blue: 3.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var slate: UIColor {
        return UIColor(red: 76.0 / 255.0, green: 82.0 / 255.0, blue: 100.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var blueGrey: UIColor {
        return UIColor(red: 137.0 / 255.0, green: 143.0 / 255.0, blue: 160.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 225.0 / 255.0, alpha: 1.0)
    }
    
}
